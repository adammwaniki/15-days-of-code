package main

import (
	"reflect"
	"testing"
)

func TestAddBinary(t *testing.T) {
	// Define a slice of test cases
	tests := []struct{
		a 		 string
		b 		 string
		expexted string
	}{
		// Test case 1: input a "11", b "1", expected output "100"
		{"11", "1", "100"},

		// Test case 2: input a "1010", b "1011", expected output "10101"
		{"1010", "1011", "10101"},

		// Test case 3: input a "10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101"
		// b "110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011"
		// output "110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000"
		{"10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101", "110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011", "110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000"},

	}

	// Iterate over the test cases
	for _, test := range tests {
		result := addBinary(test.a, test.b)

		// Return an error in case it fails
		if !reflect.DeepEqual(result, test.expexted){
			t.Errorf("addBinary(%v, %v) = %v; want: %v", test.a, test.b, result, test.expexted)
		}
	}
}